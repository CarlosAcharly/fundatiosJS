// operadores matematicos
// + y -
// * y /
// % y **
console.log(10 + 3);
console.log(10 - 3);
console.log(10 * 3);
console.log(10 / 3);
console.log(10 % 3);
console.log(10 ** 3);

// operadores string
// concatenacion +
console.log("hola" + " " + "MUNDO");

